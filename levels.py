class Map:
    def __init__(self, game) -> None:
        self.game = game
        self.tilemap = TILEMAP
        self.world_map = {}
        
        self.get_world_map(self.tilemap)

    def get_world_map(self, tilemap) -> None:
        for i, row in enumerate(tilemap):
            for j, col in enumerate(row):
                if col:
                    self.world_map[(j, i)] = col

    def draw(self) -> None:
        [pygame.draw.rect(self.game.screen, (255, 255, 255), (pos[0] * TILESIZE, pos[1] * TILESIZE, TILESIZE, TILESIZE), 2) for pos in self.world_map]